/**
 * add 2 numbers
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
*/
const add = (x, y) => x + y
/**
 * subtract y from x
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
*/
const subtract = (x, y) => x - y
/**
 * multiply 2 numbers
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
*/
const multiply = (x, y) => x * y
/**
 * divide x by y
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
*/
const divide = (x, y) => x / y

module.exports = { multiply, divide, add, subtract }
